package uml;

public class provincia {

	private String nombre;

    public provincia() {
    }

    public provincia(String nombre) {
        this.nombre = nombre;
    }

        
	public String getNombre() {
		return this.nombre;
	}

	/**
	 * 
	 * @param nombre
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}



}